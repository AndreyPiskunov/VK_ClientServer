import UIKit

extension MyFriendsController: UITableViewDataSource {

    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        
    return myFriends.count
}

    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
    
    guard let cell = tableView.dequeueReusableCell (withIdentifier: customTableViewCellIdentifire, for: indexPath) as? CustomTableViewCell
            
        else { return UITableViewCell() }
    
    let friend = myFriends [indexPath.row]
    
    cell.configure(image: UIImage(named: friend.avatar!), name: friend.name, surname: friend.surname, description: friend.age)
    
    return cell
    }
    
}
